class HelloWorld {
    public static void main(String[] args){
        System.out.println("Hello World");
        // primitive types
        //integer numbers
        byte bNumber = 4; //1 byte
        short sNumber = 56; //2 bytes
        int iNumber = 123; //4 bytes
        long lNumber = 131415125;

        System.out.println("Integer Numbers " + bNumber + " " + sNumber + " " + iNumber + " " +lNumber);
        // real numbers
        float fNumber = 1.22f;
        double dNumber = 3.456;

        // character
        char c = 'c';
        boolean b = false;
        System.out.println("Real numbers " + fNumber + " " + dNumber);
        System.out.println("Character " + c + " boolean is "+ b);

    }
}
